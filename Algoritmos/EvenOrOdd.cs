using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace C_Sharp_Study.Algoritmos
{
    public class EvenOrOdd
    {
        public static string Solution(int number)
        {
            if (number % 2 == 0) return "Even";
            else return "Odd";
        }
    }
}
